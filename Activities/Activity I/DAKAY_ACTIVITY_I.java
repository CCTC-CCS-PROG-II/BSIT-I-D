public class DAKAY_ACTIVITY_I {
    public static void main(String[] args) {
        int a = 1, b = 2, c = 3;
        int sum = a+b+c;
        int dif = a-b-c;
        int pro = a*b*c;
        int quo = a/b/c;
        System.out.println("Sum "+sum);
        System.out.println("Difference "+dif);
        System.out.println("Product "+pro);
        System.out.println("Quotient "+quo);
    }
}