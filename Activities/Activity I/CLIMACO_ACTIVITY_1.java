public class CLIMACO_ACTIVITY_1{
    public static void main(String[] args) {
        int x = 22,  y = 33,  z = 46;
           
        int sum = x + y + z;
        int diff = x - y - z;
        int prod = x * y * z;
        int quot = x / y / z;
    
        System.out.println("Sum: " + sum);
        System.out.println("Difference: " + diff);
        System.out.println("Product: " + prod);
        System.out.println("Quotient: " + quot);
    }
}