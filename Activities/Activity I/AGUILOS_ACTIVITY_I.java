public class AGUILOS_ACTIVITY_I{
    public static void main (String[]args){
       int x = 4;
       int y = 5;
       int z = 7;

       int sum = x + y + z;
       int diff = x - y - z;
       int product = x * y * z;
       int quotient = x / y / z;

       System.out.println("Sum = " + sum);
       System.out.println( "Difference = " + diff);
       System.out.println("Product = " + product);
       System.out.println("Quotient = " + quotient);
    }
}