public class CALUNSAG_ACTIVITY_I{
    public static void main(String[]args){
      int x = 9, y = 28, z = 31;
    
      int sum = x+y+z;
      int diff = x-y-z;
      int quot = x/y/z;
      int prod = x*y*z;
    

      System.out.println("Sum: " + sum);
      System.out.println("Difference: " + diff);
      System.out.println("Quotient: " + quot);
      System.out.println("Product: " + prod); 
    
      }
    }
    
    