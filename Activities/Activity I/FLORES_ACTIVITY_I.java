public class FLORES_ACTIVITY_I {
    public static void main(String[] args) {
        int x = 10, y = 20, z = 30;

        int sum = x + y + z;
        int diff = z - y -x;
        int prod = x * y * z;
        int qout = z / y/ x;

        System.out.println("Sum: "+sum);
        System.out.println("Diff: "+diff);
        System.out.println("Prod: "+prod);
        System.out.println("Qout:"+qout);
    }
}