public class ALLEGO_ACTIVITY_I{
    public static void main(String[] args){
        int a = 10, b = 20, c = 30;
     
      int sum = a + b + c;
      int diff = a - b - c;
      int product = a*b*c;
      int quotient = a/b/c;
  
      System.out.println("Sum =" + sum);
      System.out.println("Difference =" + diff);
      System.out.println("Product=" + product);
      System.out.println("Quotient =" + quotient);
      
    }
 }