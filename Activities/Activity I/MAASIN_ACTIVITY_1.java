public class MAASIN_ACTIVITY_I{
    public static void main(String []args){

        int x = 5;
        int y = 15;
        int z = 20;

        int addition = x+y+z;
        int subtarction = addition-z;
        int multiplication = addition*subtarction;
        int division = multiplication/addition;

        System.out.println("Sum= " + addition);
        System.out.println("Diff= " + subtarction);
        System.out.println("Product= " + multiplication);
        System.out.println("Quotient= " + division);
    }
}
