public class UDIAMAN_ACTIVITY_I {

 public static void main(String[] args) {
        int y = 25, x = 50, z = 75;
       
        int sum = y + x + z;
        int diff = z - x - y;
        int prod = y * z * x;
        int quot = z / y;
        
        System.out.println("Sum: " + sum);
        System.out.println("Difference: " +diff);
        System.out.println("Product: " + prod);
        System.out.println("Quotient: " + quot);
    }
}
  