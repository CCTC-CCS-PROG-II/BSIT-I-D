public class PINILI_ACTIVITY_1{
    public static void main(String []args) {
        int x = 5;
        int y = 10;
        int z = 15;
        
        int sum = x + y + z;
        int diff = x - y - z;
        int product = x * y * z;
        int quotient = x / y / z;

        System.out.println("Sum = " + sum);
        System.out.println("Difference = " + diff);
        System.out.println("Product = " + product);
        System.out.println("Quotient = " + quotient);

    }
}